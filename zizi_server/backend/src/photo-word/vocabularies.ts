/** 基础常用字（字库匹配不到时的降级选择） */
export const BASIC_WORDS = ['大', '小', '人', '口', '手', '天', '地', '水', '火', '山'];

/** 赞美字兜底（连基础字都匹配不到时使用） */
export const PRAISE_WORDS = ['好', '棒', '赞', '美', '妙', '赞', '强'];

/**
 * 从候选词列表中匹配字库
 * @returns 匹配到的单字，或降级字
 */
export function matchVocabulary(
  candidates: string[],
  vocabLevel: number,
): string {
  const vocab = VOCABULARIES[vocabLevel] ?? VOCABULARIES[1];

  // 优先匹配：在字库中出现的单字
  for (const word of candidates) {
    for (const char of word) {
      if (vocab.has(char)) return char;
    }
  }

  // 降级：尝试匹配候选词中的任意字符
  for (const word of candidates) {
    for (const char of word) {
      if (BASIC_WORDS.includes(char)) return char;
    }
  }

  // 兜底：赞美字
  return PRAISE_WORDS[Math.floor(Math.random() * PRAISE_WORDS.length)];
}
